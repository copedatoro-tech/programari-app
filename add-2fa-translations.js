let successCount = 0;
let errorCount = 0;

for (const locale of Object.keys(TWO_FACTOR)) {
  const filePath = path.join(MESSAGES_DIR, `${locale}.json`);

  if (!fs.existsSync(filePath)) {
    console.error(`Lipseste fisierul: ${filePath}`);
    errorCount++;
    continue;
  }

  try {
    const raw = fs.readFileSync(filePath, 'utf-8');
    const data = JSON.parse(raw);

    // ✅ Adăugăm secțiunea nouă "twoFactor" complet
    if (!data.twoFactor) {
      data.twoFactor = TWO_FACTOR[locale];
      console.log(`${locale}.json - sectiune "twoFactor" adaugata`);
    } else {
      console.log(`${locale}.json - "twoFactor" exista deja, sar peste`);
    }

    // ✅ Adăugăm cheile noi în "loginPage"
    if (!data.loginPage) {
      console.error(`Sectiunea "loginPage" nu exista in ${locale}.json`);
      errorCount++;
      continue;
    }
    for (const [key, value] of Object.entries(LOGIN_MFA_KEYS[locale])) {
      if (!data.loginPage[key]) {
        data.loginPage[key] = value;
      }
    }

    fs.writeFileSync(filePath, JSON.stringify(data, null, 2), 'utf-8');
    successCount++;
  } catch (err) {
    console.error(`Eroare la ${locale}.json:`, err.message);
    errorCount++;
  }
}

console.log(`\n=== Rezultat: ${successCount} fisiere actualizate, ${errorCount} erori ===`);